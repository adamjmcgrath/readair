////////////////////////////////////////////////////////////////////
// GRA.searchresults
////////////////////////////////////////////////////////////////////

if (typeof GRA == "undefined") {var GRA = {};}

GRA.searchresults = function(thePayload) {
	this.XMLdocument = LIB.dom.parseFromString(thePayload);
}

////////////////////////////////////////////////////////////////////
// GRA.searchresults Getters/Setters
////////////////////////////////////////////////////////////////////

GRA.searchresults.prototype = {
    ids: function() {
		// returns array of feed objects
		var nodes = LIB.dom.evaluateXPath(this.XMLdocument, "/object/list/object/number[@name='id']");
		return nodes;
	}
}